/**
 * 根据图标名称获取 SVG 图标组件
 * @param iconName 图标名称
 * @param className 可选的 CSS 类名
 * @param style 可选的 CSS 样式
 * @returns SVG 图标组件
 */
export const getSvgIcon = (
  iconName: string,
  className?: string,
  style?: React.CSSProperties
): React.ReactNode => {
  const normalizedName = iconName?.toLowerCase() || 'default';
  const icon = iconMap[normalizedName] || iconMap.default;

  if (!icon) return null;

  // 如果图标有子元素，给它添加 className 和 style
  if (React.isValidElement(icon)) {
    return React.cloneElement(icon as React.ReactElement<{ className?: string; style?: React.CSSProperties }>, {
      className,
      style,
    });
  }

  return icon;
};

export default getSvgIcon;
